  <div class="app-container">
    <!-- 订单统计卡片 -->
    <el-row :gutter="20" style="margin-bottom: 20px;">
      <el-col :span="8">
        <el-card class="stat-card pending-card">
          <div class="stat-content">
            <div class="stat-number">{{ formatNumber(statistics.pending) }}</div>
            <div class="stat-label">待处理订单</div>
          </div>
          <i class="el-icon-time stat-icon" />
        </el-card>
      </el-col>
      <el-col :span="8">
        <el-card class="stat-card reviewing-card">
          <div class="stat-content">
            <div class="stat-number">{{ formatNumber(statistics.reviewing) }}</div>
            <div class="stat-label">审核中订单</div>
          </div>
          <i class="el-icon-loading stat-icon" />
        </el-card>
      </el-col>
      <el-col :span="8">
        <el-card class="stat-card amount-card">
          <div class="stat-content">
            <div class="stat-number">{{ statistics.totalAmount }}</div>
            <div class="stat-label">待处理总金额(U)</div>
          </div>
          <i class="el-icon-money stat-icon" />
        </el-card>
      </el-col>
    </el-row>

    <!-- 筛选条件 -->
    <el-card style="margin-bottom: 20px;">
      <div class="filter-container">
        <el-input
          v-model="listQuery.orderNo"
          :placeholder="$t('order.orderNo')"
          style="width: 200px;"
          class="filter-item"
          @keyup.enter.native="handleFilter"
        />

        <el-select
          v-model="listQuery.status"
          placeholder="订单状态"
          clearable
          style="width: 150px"
          class="filter-item"
        >
          <el-option label="待处理" value="pending" />
        </el-select>

        <el-input
          v-model="listQuery.customer"
          :placeholder="$t('order.customer')"
          style="width: 150px;"
          class="filter-item"
          @keyup.enter.native="handleFilter"
        />

        <el-date-picker
          v-model="listQuery.dateRange"
          type="daterange"
          :range-separator="$t('common.to')"
          start-placeholder="开始日期"
          end-placeholder="结束日期"
          class="filter-item"
          style="width: 240px"
        />

        <el-button
          v-waves
          class="filter-item"
          type="primary"
          icon="el-icon-search"
          @click="handleFilter"
        >
          {{ $t('common.search') }}
        </el-button>

        <el-button
          v-waves
          class="filter-item"
          type="success"
          icon="el-icon-refresh"
          @click="refreshData"
        >
          刷新数据
        </el-button>
      </div>
    </el-card>

    <!-- 订单列表 -->
    <el-card>
      <div slot="header" class="clearfix">
        <span>待处理订单列表</span>
        <el-tag v-if="total > 0" type="warning" style="margin-left: 10px;">
          共 {{ total }} 条待处理订单
        </el-tag>
      </div>

      <el-table
        :key="tableKey"
        v-loading="listLoading"
        :data="list"
        border
        fit
        highlight-current-row
        style="width: 100%;"
        @sort-change="sortChange"
      >
        <el-table-column
          :label="$t('order.orderNo')"
          prop="orderNo"
          min-width="150"
        >
          <template slot-scope="{row}">
            <span class="link-type" @click="handleDetail(row)">{{ row.orderNo }}</span>
          </template>
        </el-table-column>
        <el-table-column
          :label="$t('order.customer')"
          prop="customerName"
          width="120"
        />
        <el-table-column
          :label="$t('order.dataType')"
          min-width="150"
        >
          <template slot-scope="{row}">
            <div class="data-type-info">
              <div>{{ row.dataInfo.country }} - {{ getValidityText(row.dataInfo.validity) }}</div>
              <div class="data-source">{{ row.dataInfo.source }}</div>
            </div>
          </template>
        </el-table-column>
        <el-table-column
          :label="$t('order.quantity')"
          prop="quantity"
          width="100"
          align="center"
        >
          <template slot-scope="{row}">
            {{ formatNumber(row.quantity) }}
          </template>
        </el-table-column>
        <el-table-column
          label="运营商"
          min-width="150"
        >
          <template slot-scope="{row}">
            <div v-for="operator in row.operators" :key="operator.name" class="operator-item">
              <span class="operator-name">{{ operator.name }}:</span>
              <span class="operator-count">{{ formatNumber(operator.count) }}</span>
            </div>
          </template>
        </el-table-column>
        <el-table-column
          :label="$t('order.amount')"
          prop="amount"
          width="100"
          align="center"
        >
          <template slot-scope="{row}">
            {{ row.amount }} U
          </template>
        </el-table-column>
        <el-table-column
          :label="$t('order.status')"
          prop="status"
          width="100"
          align="center"
        >
          <template slot-scope="{row}">
            <el-tag :type="getStatusType(row.status)">
              {{ getStatusText(row.status) }}
            </el-tag>
          </template>
        </el-table-column>
        <el-table-column
          :label="$t('order.createTime')"
          prop="createTime"
          width="150"
          align="center"
          sortable="custom"
        >
          <template slot-scope="{row}">
            {{ row.createTime | parseTime('{y}-{m}-{d} {h}:{i}') }}
          </template>
        </el-table-column>
        <el-table-column
          :label="$t('common.operation')"
          align="center"
          width="200"
          class-name="small-padding fixed-width"
        >
          <template slot-scope="{row}">
            <el-button
              type="primary"
              size="mini"
              @click="handleDetail(row)"
            >
              {{ $t('common.detail') }}
            </el-button>
            <el-button
              v-if="row.status === 'pending'"
              type="success"
              size="mini"
              @click="handleProcess(row)"
            >
              提交审核
            </el-button>
            <el-button
              size="mini"
              type="danger"
              @click="handleCancel(row)"
            >
              取消
            </el-button>
          </template>
        </el-table-column>
      </el-table>

      <!-- 分页 -->
      <pagination
        v-show="total>0"
        :total="total"
        :page.sync="listQuery.page"
        :limit.sync="listQuery.limit"
        @pagination="getList"
      />
    </el-card>

  </div>
