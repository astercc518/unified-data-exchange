<script>
import { parseTime } from '@/utils'
import Pagination from '@/components/Pagination'
import waves from '@/directive/waves'
import i18nMixin from '@/mixins/i18n'

export default {
  name: 'PendingOrders',
  components: { Pagination },
  directives: { waves },
  filters: {
    parseTime
  },
  mixins: [i18nMixin],
  data() {
    return {
      tableKey: 0,
      list: [],
      total: 0,
      listLoading: true,
      listQuery: {
        page: 1,
        limit: 20,
        orderNo: undefined,
        status: undefined,
        customer: undefined,
        dateRange: null,
        sort: '-createTime'
      },
      statistics: {
        pending: 0,
        reviewing: 0,
        totalAmount: '0.00'
      },
      deliveryDialogVisible: false,
      currentOrder: null,
      deliveryLoading: false
    }
  },
  created() {
    this.getList()
    this.getStatistics()
  },
  activated() {
    // 当组件被激活时（从其他页面返回），重新加载数据
    console.log('🔄 待处理订单页面被激活，重新加载数据...')
    this.getList()
    this.getStatistics()
  },
  methods: {
    getList() {
      this.listLoading = true
      console.log('📊 开始加载待处理订单列表...')

      // 从localStorage获取真实订单数据
      const savedOrders = localStorage.getItem('orderList')
      let orders = []

      if (savedOrders) {
        try {
          orders = JSON.parse(savedOrders)
          console.log('✅ 加载到', orders.length, '条订单')
        } catch (e) {
          console.error('解析订单数据失败:', e)
          orders = []
        }
      } else {
        console.log('⚠️ 未找到订单数据')
      }

      // 只保留待处理的订单(不包括审核中)
      orders = orders.filter(order => order.status === 'pending')

      console.log('✅ 筛选出', orders.length, '条待处理订单')

      // 转换数据格式
      let processedOrders = orders.map(order => ({
        id: order.id,
        orderNo: order.orderNo,
        customerName: order.customerName,
        dataInfo: {
          country: order.country,
          validity: order.validity,
          source: order.source
        },
        quantity: order.quantity,
        operators: order.operators || [],
        amount: parseFloat(order.totalAmount || 0).toFixed(2),
        status: order.status || 'pending',
        deliveryStatus: order.deliveryStatus || 'pending',
        deliveryEmail: order.deliveryEmail,
        createTime: order.createTime,
        deliveryTime: order.deliveryTime,
        remark: order.remark
      }))

      // 应用筛选条件
      if (this.listQuery.orderNo) {
        processedOrders = processedOrders.filter(order =>
          order.orderNo.toLowerCase().includes(this.listQuery.orderNo.toLowerCase())
        )
      }

      if (this.listQuery.status) {
        processedOrders = processedOrders.filter(order => order.status === this.listQuery.status)
      }

      if (this.listQuery.customer) {
        processedOrders = processedOrders.filter(order =>
          order.customerName.toLowerCase().includes(this.listQuery.customer.toLowerCase())
        )
      }

      if (this.listQuery.dateRange && this.listQuery.dateRange.length === 2) {
        const startTime = this.listQuery.dateRange[0].getTime()
        const endTime = this.listQuery.dateRange[1].getTime() + 24 * 60 * 60 * 1000
        processedOrders = processedOrders.filter(order => {
          const orderTime = order.createTime
          return orderTime >= startTime && orderTime < endTime
        })
      }

      // 排序
      processedOrders.sort((a, b) => {
        if (this.listQuery.sort === '-createTime') {
          return b.createTime - a.createTime
        } else if (this.listQuery.sort === '+createTime') {
          return a.createTime - b.createTime
        }
        return 0
      })

      // 分页
      this.total = processedOrders.length
      const start = (this.listQuery.page - 1) * this.listQuery.limit
      const end = start + this.listQuery.limit
      this.list = processedOrders.slice(start, end)

      console.log('✅ 待处理订单列表加载完成，显示', this.list.length, '条记录')

      setTimeout(() => {
        this.listLoading = false
      }, 300)
    },

    getStatistics() {
      // 从localStorage获取统计数据
      const savedOrders = localStorage.getItem('orderList')
      let orders = []

      if (savedOrders) {
        try {
          orders = JSON.parse(savedOrders)
        } catch (e) {
          console.error('解析订单数据失败:', e)
        }
      }

      const pending = orders.filter(o => o.status === 'pending')
      const reviewing = orders.filter(o => o.status === 'reviewing')
      const totalAmount = [...pending, ...reviewing].reduce((sum, o) => sum + parseFloat(o.totalAmount || 0), 0)

      this.statistics = {
        pending: pending.length,
        reviewing: reviewing.length,
        totalAmount: totalAmount.toFixed(2)
      }
    },

    handleFilter() {
      this.listQuery.page = 1
      this.getList()
    },

    refreshData() {
      console.log('🔄 手动刷新待处理订单...')
      this.$message({
        type: 'info',
        message: '正在刷新数据...',
        duration: 1000
      })
      this.tableKey = this.tableKey + 1
      this.getList()
      this.getStatistics()
    },

    handleDetail(row) {
      this.$router.push(`/order/detail/${row.id}`)
    },

    handleProcess(row) {
      this.$confirm('确认将该订单提交给代理审核？', this.$t('common.warning'), {
        confirmButtonText: this.$t('common.confirm'),
        cancelButtonText: this.$t('common.cancel'),
        type: 'warning'
      }).then(() => {
        // 更新订单状态为审核中
        const savedOrders = localStorage.getItem('orderList')
        if (savedOrders) {
          const orders = JSON.parse(savedOrders)
          const orderIndex = orders.findIndex(o => o.id === row.id)
          if (orderIndex !== -1) {
            orders[orderIndex].status = 'reviewing' // 审核中
            orders[orderIndex].reviewTime = Date.now() // 提交审核时间
            localStorage.setItem('orderList', JSON.stringify(orders))
            console.log('✅ 订单已提交审核:', orders[orderIndex].orderNo)
            this.$message.success('订单已提交给代理审核')
            this.getList()
            this.getStatistics()
          }
        }
      }).catch(() => {})
    },

    handleCancel(row) {
      this.$confirm('确认取消该订单？', this.$t('common.warning'), {
        confirmButtonText: this.$t('common.confirm'),
        cancelButtonText: this.$t('common.cancel'),
        type: 'warning'
      }).then(() => {
        // 更新订单状态
        const savedOrders = localStorage.getItem('orderList')
        if (savedOrders) {
          const orders = JSON.parse(savedOrders)
          const orderIndex = orders.findIndex(o => o.id === row.id)
          if (orderIndex !== -1) {
            orders[orderIndex].status = 'cancelled'
            localStorage.setItem('orderList', JSON.stringify(orders))
            this.$message.success('订单取消成功')
            this.getList()
            this.getStatistics()
          }
        }
      }).catch(() => {})
    },

    sortChange(data) {
      const { prop, order } = data
      if (prop === 'createTime') {
        this.sortByCreateTime(order)
      }
    },

    sortByCreateTime(order) {
      if (order === 'ascending') {
        this.listQuery.sort = '+createTime'
      } else {
        this.listQuery.sort = '-createTime'
      }
      this.handleFilter()
    },

    formatNumber(num) {
      return num.toLocaleString()
    },

    getValidityText(validity) {
      const validityMap = {
        '3': '3天内',
        '30': '30天内',
        '30+': '30天以上'
      }
      return validityMap[validity] || validity
    },

    getStatusType(status) {
      const statusMap = {
        pending: 'warning',
        processing: 'primary'
      }
      return statusMap[status]
    },

    getStatusText(status) {
      const statusMap = {
        pending: this.$t('order.pending'),
        processing: this.$t('order.processing')
      }
      return statusMap[status]
    }
  }
}
</script>

