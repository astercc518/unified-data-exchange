// 获取嵌套对象的值
export function getNestedValue(obj, path) {
  return path.split('.').reduce((current, key) => current && current[key], obj)
}

// 国际化翻译函数
export function t(key, lang = 'zh') {
  const message = getNestedValue(messages[lang], key)
  return message || key
}
